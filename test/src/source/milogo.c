void display_logo()
{
	unsigned char x;

	write_command(0xb0 + 2);
	write_command(((49 & 0xf0) >> 4) | 0x10);
	write_command((49 & 0x0f) | 0x01);
	for (x = 0; x < 32; x++)
		write_data(page2[x]);

	write_command(0xb0 + 3);
	write_command(((49 & 0xf0) >> 4) | 0x10);
	write_command((49 & 0x0f) | 0x01);
	for (x = 0; x < 32; x++)
		write_data(page3[x]);

	write_command(0xb0 + 4);
	write_command(((49 & 0xf0) >> 4) | 0x10);
	write_command((49 & 0x0f) | 0x01);
	for (x = 0; x < 32; x++)
		write_data(page4[x]);

	write_command(0xb0 + 5);
	write_command(((49 & 0xf0) >> 4) | 0x10);
	write_command((49 & 0x0f) | 0x01);
	for (x = 0; x < 32; x++)
		write_data(page5[x]);
	sleep_ms(10000);
	clean(0x00);
}
